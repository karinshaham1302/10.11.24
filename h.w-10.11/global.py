# global זה משתנה שהוגדר מחוץ לפונקציה
# זה אומר לפונקציה שאנו רוצים לשנות את הערך, להשתמש במשתנה הגלובאלי

# החסרונות של global שקשה להתנהל ולעקוב אחרי הקוד אם חלו בו שינויים במיוחד כשיש הרבה פונקציות
# וגם קשה לבדןק את הקוד כי התוצאות יכולות להשתנות


# כשנכתוב:

x: int = 1

def foo():
    print(x)
    x = 4


foo()

# זה קשור ל- x ה- locally
# היינו צריכים קודם לכתוב ערך ( פרמטר ) בתוך הפונקציה כי פייתון לא יודע שהתכוונו לגלובאלי
